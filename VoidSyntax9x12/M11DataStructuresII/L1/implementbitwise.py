num1 = 10
num2 = 5
 
# AND operator
print("num1 & num2 =", num1 & num2)
 
# OR operator
print("\nnum1 | num2 =", num1 | num2)
 
# NOT operator
print("\n~num1 =", ~num1)
 
# XOR operator
print("\nnum1 ^ num2 =", num1 ^ num2)
 
# Right Shift operator on num1
print("\nnum1 >> 1 =", num1 >> 1)
 
# Right Shift operator on num2
print("\nnum2 >> 1 =", num2 >> 1)
 

# Left Shift  operator on num1
print("\nnum1 << 1 =", num1 << 1)
 
# Left Shift operator on num2
print("\nnum2 << 1 =", num2 << 1)
